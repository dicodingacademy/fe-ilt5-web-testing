const Calculator = {
  addTwoNumber: (number1, number2) => {
    return number1 + number2;
  },

  subtractTwoNumber: (number1, number2) => {
    return number1 - number2;
  },

  multiplyTwoNumber: (number1, number2) => {
    return number1 * number2;
  },

  divideTwoNumber: (number1, number2) => {
    return number1 / number2;
  },
};

export default Calculator;
